package com.guillevier.resumen.clases;

// LISTO
class Variables {
  public static void main(String[] args) {
    // Lower Camel Case
    int soyUnNumeroInt = 10;
    // Constant
    int _SOYUNACONSTANTE = 10;
    // Declaring a variable
    int age;
    int speed;
    int salary;
    // Assigning a value to the variable
    age = 31;
    speed = 20;
    salary = 1000;
    // Integer
    int n = 1234567890;
    // Long
    long nL = 1234567890123456789L;
    // Double
    double nD = 123.4567890;
    // Float
    float nF = 123.4567890F;
    // Declaring and Assigning a variable
    String employeeName = "Cindy Oñate";

    // Printing the value of the variable
    System.out.println("Nombre: " + employeeName + " Edad: " + age + " Velocidad de reacción: " + speed);
  }
}